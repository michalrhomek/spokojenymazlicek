function getQuestions (e){
	var data = {
		action: 'getQuestions',
		tabName: 'questions',
		tabFolder: 'classes/tabs/',
		dataType: 'html'
	};

	var _json = {
		data: data,
		beforeSend: function () {
			$('div#idTab_questions div.action-overlay').fadeIn('slow');
		},
		success: function(htmlData) {
			$("#questions-content").html(htmlData);
		},
		complete: function () {
			$('div#idTab_questions div.action-overlay').fadeOut('slow');
		}
	};
	makeRequest(_json);
}

$(document).ready(function () {
	$('div#idTab_questions').append('<div class="action-overlay" style="display: none;"></div>');
	$('a[href="#idTab_questions"]').on('click', function(e){getQuestions(e);});
});